from math import * #Biblioteca com mais funções matemáticas

num = 5

print(num) #Printa o número
print(pow(num, 2)) #Printa uma potência, no caso, "num" ao quadrado
print(max(10, 20)) #Printa qual dos dois é maior
print(min(140, 1)) #Printa qual dos dois é menor
print(round(3.7)) #Aproxima o número de dentro dos parênteses

print(floor(3.7)) #Arredonda pra baixo
print(ceil(3.7)) #Arredonda pra cima
print(sqrt(36)) #Printa a raiz do número de dentro