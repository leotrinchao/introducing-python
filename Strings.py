frase = "Coe Rapaziada"

print(frase) #Printa a frase do jeito que é
print(frase.lower()) #Printa tudo em minúsculo
print(frase.upper()) #Printa tudo em maiúsculo
print(len(frase)) #Printa o número de letras na frase
print(frase[4]) #Printa a letra na posição 4, começando de 0
print(frase.index("a")) #Printa a posição que o primeiro "a" aparece
print(frase.replace("Rapaziada", "Galerinha")) #Muda uma frase ou letra e printa
print(frase.count("a")) #Conta quantas letras "a" tem na frase
